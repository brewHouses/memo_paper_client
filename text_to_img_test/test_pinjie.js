var ascii = ['a','b','c','d','e','f','g','h','i','j','k','l','m','n','o','p']

function handle_bin_img(img){
    length = img.length
    var ret = ""
    temp_index = 1
    temp = 0
    for(let i=0; i < length; i++){
        if(img[i] == 255)
            temp += Math.pow(2, temp_index-1)
        if(temp_index % 4 == 0){
            temp_index = 1;
            ret += ascii[temp]
            temp = 0
            continue
        }
        temp_index++
    }
    return ret;
}

